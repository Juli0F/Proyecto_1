    public void porTienda(java.sql.Date dateInit, java.sql.Date dateFinal) {
        tiendas.forEach(buscar -> {
            reporte.addAll(manager.getProductoDAO().getSinVender(buscar, dateInit, dateFinal));
        });
    }

    public void fillTable(List<SinVender> sinVender) {
        ((DefaultTableModel) tablePrincipal.getModel()).setRowCount(0);

        sinVender.forEach(datos -> {
            ((DefaultTableModel) tablePrincipal.getModel()).addRow(new Object[]{
                datos.getCodigoTienda(),
                datos.getNombreTienda(),
                datos.getCodigoProducto(),
                datos.getNombreProducto()
            });

        });
                
    }

    public void datos() {
        LocalDate dateInit;
        LocalDate dateFinal;
        java.sql.Date inicio = null;
        java.sql.Date fechaFinal = null;

        if (!txtDateInit.getText().isEmpty()) {
            String[] date = txtDateInit.getText().split("/");
            dateInit = LocalDate.of(Integer.valueOf(date[2]), Integer.valueOf(date[1]), Integer.valueOf(date[0]));
            inicio = java.sql.Date.valueOf(dateInit);

        }
        if (!txtDateFinal.getText().isEmpty()) {
            String[] date = txtDateFinal.getText().split("/");
            dateFinal = LocalDate.of(Integer.valueOf(date[2]), Integer.valueOf(date[1]), Integer.valueOf(date[0]));
            fechaFinal = java.sql.Date.valueOf(dateFinal);
        }
        getListadoTiendas();
        porTienda(inicio, fechaFinal);
        //java.sql.Date.valueOf(dateInit);
        verificar();

    }

    public void verificar() {

        if (reporte.size() != 0) {

             fillTable(reporte);
        } else {
            JOptionPane.showMessageDialog(null, "No Hay Datos Para Mostrar", "informacion", JOptionPane.INFORMATION_MESSAGE);

        }
    }

    private void getListadoTiendas() {

        manager.getTiendaDAO().obtenerTodo().forEach(t -> {
            tiendas.add(t.getCodigo());
        });

    }

