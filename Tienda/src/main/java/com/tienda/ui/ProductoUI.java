    public void fillTable(List<Producto> listadoTienda) {
        DefaultTableModel tableModel = (DefaultTableModel) tableView.getModel();
        tableModel.setNumRows(0);

        listadoTienda.forEach(tiendax -> {
            tableModel.addRow(new Object[]{
                tiendax.getCodigo(),
                tiendax.getNombre(),
                tiendax.getFabricante(),
                tiendax.getDescripcion(),
                tiendax.getGarantia(),});
        });

        tableView.setModel(tableModel);

    }
