    private void accionTable() {
        tableView.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                row = tableView.rowAtPoint(evt.getPoint());
                int col = tableView.columnAtPoint(evt.getPoint());

                codigo = (String) tableView.getModel().getValueAt(row, 0);
                nombre = (String) tableView.getModel().getValueAt(row, 1);
                descripcion = (String) tableView.getModel().getValueAt(row, 2);
                fabricante = (String) tableView.getModel().getValueAt(row, 3);
                garantia = (int) tableView.getModel().getValueAt(row, 4);
                System.out.println("Codigo" + codigo);

                setTextField(codigo, nombre, descripcion, fabricante, String.valueOf(garantia));
            }
        });

    }

    private boolean checkField() {
        lblFieldCode.setVisible(txtCodigo.getText().isEmpty());
        lblFieldDIreccion.setVisible(txtFabricante.getText().isEmpty());
        lblFieldNombre.setVisible(txtNombre.getText().isEmpty());

        return !txtCodigo.getText().isEmpty()
                && !txtFabricante.getText().isEmpty()
                && !txtNombre.getText().isEmpty();

    }

    private void setTextField(String codigo, String nombre, String direccion, String descripcion, String horario) {
        txtCodigo.setText(codigo);
        txtNombre.setText(nombre);
        txtFabricante.setText(direccion);

        txtDescripcion.setText(descripcion);
        txtGarantia.setText(horario);
    }

    public void evento() {
        txtSearch.addKeyListener(new KeyAdapter() {
            public void keyTyped(KeyEvent e) {
                char caracter = e.getKeyChar();

                // Verificar si la tecla pulsada no es un digito
                buscar += caracter; 
               fillTable(manager.getProductoDAO().getSearchWithLike(buscar));
                if (((caracter < '0')
                        || (caracter > '9'))
                        && (caracter != '\b' /*corresponde a BACK_SPACE*/)) {
                    e.consume();  // ignorar el evento de teclado
                }
            }
        });
    }
