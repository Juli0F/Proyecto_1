    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtCodigo.isEditable()) {
            if (checkField()) {
                var garantiaa = (txtGarantia.getText().isEmpty()) ? "0" : txtGarantia.getText();
                Producto producto = new Producto(
                        txtCodigo.getText(),
                        txtNombre.getText(),
                        txtFabricante.getText(),
                        txtDescripcion.getText(),
                        Integer.valueOf(garantiaa), true);

                manager.getProductoDAO().insert(producto);
                JOptionPane.showMessageDialog(null, "Se Modifico Correctamente", "Informacion", JOptionPane.INFORMATION_MESSAGE);

                DefaultTableModel tableModel = (DefaultTableModel) tableView.getModel();
                tableModel.addRow(new Object[]{producto.getCodigo(), producto.getNombre(), producto.getFabricante(), producto.getDescripcion(), producto.getGarantia()});

                tableModel.removeRow(row);
                setTextField("", "", "", "", "");
                txtCodigo.setEditable(false);

            }

        } else {
            if (checkField()) {
                //codigo, String nombre, String direccion, String telefono, String telefono2, String email, String horario, boolean estado) {
                if (manager.getTiendaDAO().obtener(txtCodigo.getText()) == null) {

                    Producto producto = new Producto(
                            txtCodigo.getText(),
                            txtNombre.getText(),
                            txtFabricante.getText(),
                            txtDescripcion.getText(),
                            Integer.valueOf(txtGarantia.getText()), true);

                    manager.getProductoDAO().insert(producto);
                    JOptionPane.showMessageDialog(null, "Se Ingreso Correctamente", "Informacion", JOptionPane.INFORMATION_MESSAGE);

                    DefaultTableModel tableModel = (DefaultTableModel) tableView.getModel();
                    tableModel.addRow(new Object[]{producto.getCodigo(), producto.getNombre(), producto.getFabricante(), producto.getDescripcion(), producto.getGarantia()});

                    setTextField("", "", "", "", "");

                } else {
                    JOptionPane.showMessageDialog(null, "El Codigo Ingresado ya Existe", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                }
                txtCodigo.setEditable(false);
            }
        }
        txtCodigo.setEditable(false);
    }//GEN-LAST:event_btnGuardarActionPerformed

