    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtCodigo.isEditable()) {
            if (checkField()) {
                Tienda tienda = new Tienda(
                        txtCodigo.getText(),
                        txtNombre.getText(),
                        txtDireccion.getText(),
                        txtTelefono.getText(),
                        txtTelefono2.getText(),
                        txtEmail.getText(),
                        txtHorario.getText(), true);

                manager.getTiendaDAO().insert(tienda);
                JOptionPane.showMessageDialog(null, "Se Modifico Correctamente", "Informacion", JOptionPane.INFORMATION_MESSAGE);

                DefaultTableModel tableModel = (DefaultTableModel) tableView.getModel();
                tableModel.addRow(new Object[]{tienda.getCodigo(), tienda.getNombre(), tienda.getDireccion(), tienda.getTelefono(), tienda.getTelefono2(), tienda.getEmail(), tienda.getHorario()});

                tableModel.removeRow(row);
                setTextField("", "", "", "", "", "", "");

            }

        } else {
            if (checkField()) {
                //codigo, String nombre, String direccion, String telefono, String telefono2, String email, String horario, boolean estado) {
                if (manager.getTiendaDAO().obtener(txtCodigo.getText()) == null) {

                    Tienda tienda = new Tienda(
                            txtCodigo.getText(),
                            txtNombre.getText(),
                            txtDireccion.getText(),
                            txtTelefono.getText(),
                            txtTelefono2.getText(),
                            txtEmail.getText(),
                            txtHorario.getText(), true);

                    manager.getTiendaDAO().insert(tienda);
                    JOptionPane.showMessageDialog(null, "Se Ingreso Correctamente", "Informacion", JOptionPane.INFORMATION_MESSAGE);

                    DefaultTableModel tableModel = (DefaultTableModel) tableView.getModel();
                    tableModel.addRow(new Object[]{tienda.getCodigo(), tienda.getNombre(), tienda.getDireccion(), tienda.getTelefono(), tienda.getTelefono2(), tienda.getEmail(), tienda.getHorario()});

                    setTextField("", "", "", "", "", "", "");

                } else {
                    JOptionPane.showMessageDialog(null, "El Codigo Ingresado ya Existe", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }
        txtCodigo.setEditable(false);
    }//GEN-LAST:event_btnGuardarActionPerformed

