    public void fillTable(List<Tienda> listadoTienda) {
        DefaultTableModel tableModel = (DefaultTableModel) tableView.getModel();
        tableModel.setNumRows(0);

        listadoTienda.forEach(tiendax -> {
            tableModel.addRow(new Object[]{
                tiendax.getCodigo(),
                tiendax.getNombre(),
                tiendax.getDireccion(),
                tiendax.getTelefono(),
                tiendax.getTelefono2(),
                tiendax.getEmail(),
                tiendax.getHorario()});
        });

        tableView.setModel(tableModel);

    }
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        if (JOptionPane.showConfirmDialog(null, "Seguro que desea elimiar la tienda\n Codigo " + txtCodigo.getText() + "con Nombre: " + txtNombre.getText(), "Advertencia", JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE) == 0) {
            Tienda tienda = new Tienda(codigo, nombre, direccion, telefono, telefono2, email, horario, false);
            manager.getTiendaDAO().modify(tienda);
            DefaultTableModel model = (DefaultTableModel) tableView.getModel();
            model.removeRow(row);
            setTextField("", "", "", "", "", "", "");
            JOptionPane.showMessageDialog(null, "Eliminacion exitosa", "Eliminar", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

