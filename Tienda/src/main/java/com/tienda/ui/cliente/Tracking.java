    public void buscarOrder(String order) {
        if (txtBuscarPedido.getText().isEmpty()) {
            reporte.addAll(manager.getPedidoDAO().paraMostrarAlClienteOrdenado(nitCliente, Log.codigoTienda, inicio, fechaFinal, " ORDER BY dias " + order));

        } else {
            if (check.isSelected()) {
                ///reporte.addAll(manager.getPedidoDAO().paraMostrarAlClienteOrdenado(nitCliente, Log.codigoTienda, inicio, fechaFinal," and p.codigo LIKE \"%" + txtBuscarPedido.getText() + "%\" ORDER BY dias " + order));
                reporte.addAll(manager.getPedidoDAO().paraMostrarAlClienteOrdenado(nitCliente, Log.codigoTienda, inicio, fechaFinal, " and p.codigo LIKE \"%" + txtBuscarPedido.getText() + "%\" ORDER BY dias " + order));
            } else {
                reporte.addAll(manager.getPedidoDAO().paraMostrarAlClienteOrdenado(nitCliente, Log.codigoTienda, inicio, fechaFinal, " or p.codigo LIKE \"%" + txtBuscarPedido.getText() + "%\" ORDER BY dias " + order));
            }

        }
    }
